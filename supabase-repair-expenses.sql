-- ตารางค่าซ่อมเครื่องจักรและยานยนต์
CREATE TABLE IF NOT EXISTS repair_expenses (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  year int NOT NULL,
  month int NOT NULL DEFAULT 0, -- 0=รวมทั้งปี, 1-12=รายเดือน
  section text NOT NULL,
  amount numeric NOT NULL DEFAULT 0,
  note text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE repair_expenses ENABLE ROW LEVEL SECURITY;
CREATE POLICY "allow all" ON repair_expenses FOR ALL USING (true) WITH CHECK (true);

